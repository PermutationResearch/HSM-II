#!/usr/bin/env python3
"""
update_company_packs.py — check or refresh company packs against their upstream source.

Each pack under ~/.hsm/company-packs/paperclipai/companies/ was generated by Paperclip's
company-creator skill from an upstream GitHub skill repo. This script checks each upstream
repo for commits newer than the companies-lock.json locked_at date and reports stale packs.

Usage:
    python3 scripts/update_company_packs.py              # check all
    python3 scripts/update_company_packs.py gstack       # check one
    python3 scripts/update_company_packs.py --fetch gstack   # pull latest files for one pack

Requirements:
    gh CLI (authenticated) — or set GITHUB_TOKEN env var for curl fallback

Exit codes (check mode):
    0 — all packs current (or no upstream tracked)
    1 — one or more packs are behind their upstream source
    2 — GitHub API unavailable

Exit codes (--fetch mode):
    0 — always (non-fatal; errors are logged to stderr)
"""

import json
import os
import shutil
import subprocess
import sys
import tempfile
import urllib.request
import urllib.error
from datetime import datetime, timezone
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
LOCK_FILE = REPO_ROOT / "companies-lock.json"
PACKS_ROOT = Path.home() / ".hsm" / "company-packs" / "paperclipai" / "companies"

# Upstream GitHub owner/repo for each pack
UPSTREAM = {
    "aeon-intelligence":        "aaronjmars/aeon",
    "agency-agents":            "msitarzewski/agency-agents",
    "agentsys-engineering":     "agent-sh/agentsys",
    "clawteam-capital":         "HKUDS/ClawTeam",
    "clawteam-engineering":     "HKUDS/ClawTeam",
    "clawteam-research-lab":    "HKUDS/ClawTeam",
    "donchitos-game-studio":    "Donchitos/Claude-Code-Game-Studios",
    "fullstack-forge":          "jeffallan/claude-skills",
    "gstack":                   "garrytan/gstack",
    "kdense-science-lab":       "K-Dense-AI/claude-scientific-skills",
    "minimax-studio":           "MiniMax-AI/skills",
    "product-compass-consulting": "phuryn/pm-skills",
    "redoak-review":            "OneRedOak/claude-code-workflows",
    "superpowers":              "obra/superpowers",
    "taches-creative":          "glittercowboy/taches-cc-resources",
    "trail-of-bits-security":   "trailofbits/skills",
    # apex-systems, compound-engineering-co: no upstream URL in COMPANY.md
}

RED   = "\033[0;31m"
GREEN = "\033[0;32m"
YELLOW= "\033[1;33m"
BOLD  = "\033[1m"
NC    = "\033[0m"


def gh_available() -> bool:
    try:
        r = subprocess.run(["gh", "auth", "status"], capture_output=True)
        return r.returncode == 0
    except FileNotFoundError:
        return False


def fetch_latest_commit(repo: str):
    url = f"https://api.github.com/repos/{repo}/commits?per_page=1"
    headers = {"Accept": "application/vnd.github+json"}

    token = os.environ.get("GITHUB_TOKEN", "")

    if gh_available():
        try:
            r = subprocess.run(
                ["gh", "api", f"repos/{repo}/commits?per_page=1", "--jq",
                 ".[0] | {sha: .sha[:8], date: .commit.committer.date, message: (.commit.message | split(\"\\n\")[0])}"],
                capture_output=True, text=True, timeout=10
            )
            if r.returncode == 0 and r.stdout.strip():
                return json.loads(r.stdout.strip())
        except Exception:
            pass

    if token:
        headers["Authorization"] = f"token {token}"

    try:
        req = urllib.request.Request(url, headers=headers)
        with urllib.request.urlopen(req, timeout=10) as resp:
            data = json.loads(resp.read())
            c = data[0]
            return {
                "sha": c["sha"][:8],
                "date": c["commit"]["committer"]["date"],
                "message": c["commit"]["message"].split("\n")[0][:70],
            }
    except Exception:
        return None


def locked_at() -> str:
    if LOCK_FILE.exists():
        return json.loads(LOCK_FILE.read_text()).get("locked_at", "1970-01-01T00:00:00Z")
    return "1970-01-01T00:00:00Z"


def fetch_pack(slug: str) -> int:
    """Pull latest files from upstream into the local pack.  Always returns 0 (non-fatal)."""
    upstream = UPSTREAM.get(slug)
    if not upstream:
        print(f"fetch {slug}: no upstream tracked, skipping", file=sys.stderr)
        return 0

    pack_dir = PACKS_ROOT / slug
    if not pack_dir.is_dir():
        print(f"fetch {slug}: local pack not found at {pack_dir}, skipping", file=sys.stderr)
        return 0

    clone_url = f"https://github.com/{upstream}.git"
    print(f"fetch {slug}: cloning {clone_url} ...", file=sys.stderr)

    with tempfile.TemporaryDirectory(prefix="hsm_fetch_") as tmp:
        tmp_path = Path(tmp)
        r = subprocess.run(
            ["git", "clone", "--depth", "1", "--quiet", clone_url, str(tmp_path / "repo")],
            capture_output=True, text=True, timeout=60,
        )
        if r.returncode != 0:
            print(f"fetch {slug}: git clone failed — {r.stderr.strip()}", file=sys.stderr)
            return 0

        src_root = tmp_path / "repo"
        copied = updated = skipped = 0

        # Sync markdown files under skills/, agents/, and top-level *.md
        patterns = list(src_root.glob("skills/**/*.md")) + \
                   list(src_root.glob("agents/**/*.md")) + \
                   list(src_root.glob("*.md"))

        for src_file in patterns:
            rel = src_file.relative_to(src_root)
            dst_file = pack_dir / rel
            try:
                src_bytes = src_file.read_bytes()
                if dst_file.exists():
                    if dst_file.read_bytes() == src_bytes:
                        skipped += 1
                        continue
                    dst_file.write_bytes(src_bytes)
                    updated += 1
                else:
                    dst_file.parent.mkdir(parents=True, exist_ok=True)
                    dst_file.write_bytes(src_bytes)
                    copied += 1
            except Exception as exc:
                print(f"fetch {slug}: could not copy {rel}: {exc}", file=sys.stderr)

        print(
            f"fetch {slug}: done — {copied} new, {updated} updated, {skipped} unchanged",
            file=sys.stderr,
        )
    return 0


def main() -> int:
    # --fetch <slug> mode: update one pack's files from upstream, then exit 0
    if len(sys.argv) >= 3 and sys.argv[1] == "--fetch":
        return fetch_pack(sys.argv[2])

    filter_company = sys.argv[1] if len(sys.argv) > 1 else ""

    if not PACKS_ROOT.exists():
        print(f"ERROR: packs root not found: {PACKS_ROOT}", file=sys.stderr)
        return 2

    if not gh_available() and not os.environ.get("GITHUB_TOKEN"):
        print("ERROR: need 'gh' (authenticated) or GITHUB_TOKEN env var", file=sys.stderr)
        return 2

    lock_date = locked_at()
    print(f"Companies-lock date : {lock_date}")
    print(f"Checking {len(UPSTREAM)} upstream repos...\n")

    stale, current, unknown, unreachable = [], [], [], []

    for company_dir in sorted(PACKS_ROOT.iterdir()):
        name = company_dir.name
        if filter_company and name != filter_company:
            continue

        upstream = UPSTREAM.get(name)
        if not upstream:
            print(f"  {YELLOW}?{NC}  {name:<42} no upstream tracked")
            unknown.append(name)
            continue

        commit = fetch_latest_commit(upstream)
        if not commit:
            print(f"  {YELLOW}?{NC}  {name:<42} could not reach github.com/{upstream}")
            unreachable.append(name)
            continue

        commit_date = commit["date"]
        commit_sha  = commit["sha"]
        commit_msg  = commit["message"]

        if commit_date > lock_date:
            print(f"  {RED}STALE{NC} {name:<38} upstream: {upstream}")
            print(f"        latest commit : {commit_sha}  {commit_date}")
            print(f"        message       : \"{commit_msg}\"")
            stale.append((name, upstream))
        else:
            print(f"  {GREEN}OK{NC}    {name:<42} {commit_sha} @ {commit_date}")
            current.append(name)

    print(f"\nSummary: {len(current)} current, {len(stale)} stale, "
          f"{len(unknown)} no-upstream, {len(unreachable)} unreachable")

    if stale:
        print(f"\n{BOLD}Stale packs need re-install from the Paperclip marketplace:{NC}")
        for name, upstream in stale:
            print(f"  paperclip install {name}   # upstream: {upstream}")
        print(f"\nAfter re-installing, re-lock:")
        print(f"  python3 scripts/check_company_drift.py --update")
        print(f"\nThen re-run affected yc-bench benchmarks:")
        print(f"  cargo run --bin hsm_outer_loop -- external-batch --spec config/external_yc_bench_seed7.json")
        return 1

    return 0


if __name__ == "__main__":
    sys.exit(main())
