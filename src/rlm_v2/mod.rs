//! RLM-V2: Recursive Language Model Runtime for HSM-II
//!
//! Rust-native implementation of the Recursive Language Model pattern from
//! arXiv:2512.24601, adapted for the HSM-II agent architecture.
//!
//! ## Core Concept
//! Instead of dumping large context into a single LLM call, the model:
//! 1. Generates "code" (tool calls) to process context
//! 2. Chunks/slices context as needed
//! 3. Dispatches parallel sub-queries via `llm_query()`
//! 4. Builds up answers across iterations
//! 5. Calls `FINAL(answer)` to terminate
//!
//! ## Integration with HSM-II
//! - Uses existing Tool system for "code execution"
//! - Council deliberation for parallel sub-query dispatch
//! - LadybugDB for trajectory persistence
//! - CASS for skill learning from RLM patterns
//!
//! ## Example Usage
//! ```rust
//! let rlm = RlmRuntime::new(config);
//! let result = rlm
//!     .with_context(Context::from_file("large_document.txt").await?)
//!     .execute("Summarize the key findings in this research paper")
//!     .await?;
//! ```

