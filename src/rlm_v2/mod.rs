/// Maximum iterations before forcing termination
pub const DEFAULT_MAX_ITERATIONS: usize = 20;

/// Maximum recursive depth for sub-agents
pub const DEFAULT_MAX_DEPTH: usize = 3;

/// Maximum parallel sub-queries per iteration
pub const DEFAULT_MAX_SUB_QUERIES: usize = 50;

/// Output truncation limit
pub const DEFAULT_TRUNCATE_LEN: usize = 5000;

/// A sub-query request - analogous to RLM's llm_query()
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct SubQuery {
    pub id: String,
    pub chunk_index: usize,
    pub instruction: String,
    pub context_preview: String,
    pub depth: usize,
}

/// Result from a sub-query execution
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct SubQueryResponse {
    pub query_id: String,
    pub result: String,
    pub tokens_used: Option<usize>,
    pub duration_ms: u64,
}

/// Final answer signaling completion
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct FinalAnswer {
    pub answer: String,
    pub confidence: f64,
    pub reasoning: Vec<String>,
}

/// Action that the RLM can take in an iteration
#[derive(Clone, Debug, Serialize, Deserialize)]
#[serde(tag = "action", rename_all = "snake_case")]
pub enum RlmAction {
    /// Execute tool calls (the "code" generated by LLM)
    ExecuteTools {
        tool_calls: Vec<RlmToolCall>,
    },
    /// Dispatch sub-queries on context chunks
    SubQueries {
        queries: Vec<SubQuery>,
    },
    /// Signal final answer
    Final { answer: FinalAnswer },
    /// Request more context/information
    RequestContext { request: String },
}

/// A tool call within the RLM runtime
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct RlmToolCall {
    pub tool_name: String,
    pub parameters: serde_json::Value,
    pub call_id: String,
}

/// Result of a tool execution in RLM
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct RlmToolResult {
    pub call_id: String,
    pub success: bool,
    pub output: String,
    pub error: Option<String>,
}

/// Statistics for an RLM execution
#[derive(Clone, Debug, Default, Serialize, Deserialize)]
pub struct RlmStats {
    pub total_iterations: usize,
    pub total_sub_queries: usize,
    pub total_tool_calls: usize,
    pub total_tokens_input: usize,
    pub total_tokens_output: usize,
    pub total_duration_ms: u64,
    pub cache_hits: usize,
    pub cache_misses: usize,
}

/// Error types for RLM operations
#[derive(Debug)]
pub enum RlmError {
    MaxIterationsExceeded(usize),
    MaxDepthExceeded(usize),
    ContextTooLarge(usize, usize),
    ToolExecutionFailed(String),
    LlmQueryFailed(String),
    InvalidAction(String),
    StorageError(String),
    Timeout,
}

impl std::fmt::Display for RlmError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            RlmError::MaxIterationsExceeded(n) => write!(f, "Max iterations ({}) exceeded", n),
            RlmError::MaxDepthExceeded(n) => write!(f, "Max recursion depth ({}) exceeded", n),
            RlmError::ContextTooLarge(size, max) => write!(f, "Context too large: {} bytes (max {})", size, max),
            RlmError::ToolExecutionFailed(e) => write!(f, "Tool execution failed: {}", e),
            RlmError::LlmQueryFailed(e) => write!(f, "LLM query failed: {}", e),
            RlmError::InvalidAction(e) => write!(f, "Invalid action: {}", e),
            RlmError::StorageError(e) => write!(f, "Storage error: {}", e),
            RlmError::Timeout => write!(f, "Execution timeout"),
        }
    }
}

impl std::error::Error for RlmError {}

/// Helper to generate a unique query ID
pub fn generate_query_id() -> String {
    format!("rq_{}", uuid::Uuid::new_v4().to_string()[..8].to_string())
}

/// Helper to truncate output for LLM consumption
pub fn truncate_for_llm(output: &str, max_len: usize) -> String {
    if output.len() <= max_len {
        output.to_string()
    } else {
        let truncated = &output[..max_len.saturating_sub(100)];
        format!("{}\n\n[... {} bytes truncated ...]", truncated, output.len() - max_len + 100)
    }
}

/// Build the system prompt for the RLM
pub fn build_rlm_system_prompt(config: &RlmConfig) -> String {
    format!(r#"You are the RLM (Recursive Language Model) runtime.

Your task is to process large contexts by generating "code" (tool calls) and dispatching sub-queries.

AVAILABLE FUNCTIONS:
1. Tool Calls - Execute tools to process/transform data:
   - read_file, write_file, grep, find, bash, web_search, etc.

2. Sub-Queries - Dispatch parallel queries on context chunks:
   - Use llm_query(chunk, instruction) conceptually
   - The system will execute these in parallel
   - Results feed back into next iteration

3. FINAL(answer) - Signal completion with your answer

RULES:
- Process context iteratively across multiple turns
- Chunk large contexts and query pieces in parallel
- Use tools to filter/transform before querying
- Build up answers progressively
- Call FINAL when you have the complete answer
- Max iterations: {}
- Max sub-queries per iteration: {}

RESPONSE FORMAT:
Respond with a JSON action:
{{"action": "execute_tools", "tool_calls": [...]}}
{{"action": "sub_queries", "queries": [...]}}
{{"action": "final", "answer": {{"answer": "...", "confidence": 0.95, "reasoning": [...]}}}}

Be efficient. Use parallel sub-queries for independent questions."#,
        config.max_iterations,
        config.max_sub_queries
    )
}
